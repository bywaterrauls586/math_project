function getRandomNumber() {
    return rand(1, 100);
}

function getSum($num1, $num2) {
    return $num1 + $num2;
}

function getDifference($num1, $num2) {
    return $num1 - $num2;
}

function getProduct($num1, $num2) {
    return $num1 * $num2;
}

function getQuotient($num1, $num2) {
    return $num1 / $num2;
}
